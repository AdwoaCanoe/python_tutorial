# Velocity

initial_displacement = float(input("Enter initial displacement in meters: "))

final_displacement = float(input("Enter final displacement in meters: "))

initial_time = float(input("Enter initial time in seconds : "))

final_time = float(input("Enter initial displacement in seconds: "))

velocity = (final_displacement - initial_displacement) / (final_time - initial_time)

print("velocity is: ", velocity,"m/s")